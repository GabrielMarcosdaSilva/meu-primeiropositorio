#1 - Faça um Programa que peça dois números e imprima o maior deles.

num1 = int(input('Digite um numero: '))

num2 = int(input('Digite um segundo numero: '))

if num1 > num2:
    print(f'O primeiro numero gitirado é maior, número digitado foi:{num1} ')
else:
    print(f'O segundo número é maior, número digitado foi:{num2}')